def get_stock_ticker(search_query):
    """
    Get the ticker symbol for a stock based on a search query.
    
    Args:
    search_query (str): The stock name or partial name to search for.
    
    Returns:
    str: The ticker symbol for the selected stock, or None if no match is found.
    """
    search_query = search_query.strip().title()
    filtered_stocks = [name for name in stock_mapping.keys() if search_query in name]
    
    if filtered_stocks:
        return stock_mapping[filtered_stocks[0]]  # Return the first match
    else:
        return None
    
def get_stock_suggestions(search_query):
    """
    Get a list of stock names that match the search query.
    
    Args:
    search_query (str): The stock name or partial name to search for.
    
    Returns:
    list: A list of matching stock names.
    """
    search_query = search_query.strip().title()
    return [name for name in stock_mapping.keys() if search_query in name]